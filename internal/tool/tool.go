type symbolMatch struct {
	Path    string
	Line    int
	Preview string
	Kind    string
	Score   int
}

func findSymbolMatches(absPath, relPath, content, symbol string) []symbolMatch {
	ext := strings.ToLower(filepath.Ext(absPath))
	if ext == ".go" {
		if matches := findGoSymbolMatches(absPath, relPath, content, symbol); len(matches) > 0 {
			return matches
		}
	}
	return findPatternSymbolMatches(relPath, content, symbol)
}

func findGoSymbolMatches(absPath, relPath, content, symbol string) []symbolMatch {
	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, absPath, content, parser.ParseComments)
	if err != nil {
		return nil
	}
	lines := strings.Split(content, "\n")
	var matches []symbolMatch
	for _, decl := range file.Decls {
		switch d := decl.(type) {
		case *ast.FuncDecl:
			if d.Name == nil || d.Name.Name != symbol {
				continue
			}
			kind := "func"
			score := 95
			if d.Recv != nil {
				kind = "method"
				score = 100
			}
			line := fset.Position(d.Pos()).Line
			matches = append(matches, symbolMatch{
				Path:    relPath,
				Line:    line,
				Preview: previewLine(lines, line),
				Kind:    kind,
				Score:   score,
			})
		case *ast.GenDecl:
			for _, spec := range d.Specs {
				switch s := spec.(type) {
				case *ast.TypeSpec:
					if s.Name.Name != symbol {
						continue
					}
					line := fset.Position(s.Pos()).Line
					matches = append(matches, symbolMatch{
						Path:    relPath,
						Line:    line,
						Preview: previewLine(lines, line),
						Kind:    "type",
						Score:   90,
					})
				case *ast.ValueSpec:
					for _, name := range s.Names {
						if name.Name != symbol {
							continue
						}
						line := fset.Position(name.Pos()).Line
						kind := "var"
						score := 75
						if d.Tok == token.CONST {
							kind = "const"
							score = 80
						}
						matches = append(matches, symbolMatch{
							Path:    relPath,
							Line:    line,
							Preview: previewLine(lines, line),
							Kind:    kind,
							Score:   score,
						})
					}
				}
			}
		}
	}
	return matches
}

func findPatternSymbolMatches(relPath, content, symbol string) []symbolMatch {
	type candidate struct {
		re    *regexp.Regexp
		kind  string
		score int
	}
	candidates := []candidate{
		{regexp.MustCompile(`^\s*def\s+` + regexp.QuoteMeta(symbol) + `\b`), "function", 95},
		{regexp.MustCompile(`^\s*class\s+` + regexp.QuoteMeta(symbol) + `\b`), "class", 92},
		{regexp.MustCompile(`^\s*func\s+(?:\([^)]+\)\s*)?` + regexp.QuoteMeta(symbol) + `\b`), "func", 95},
		{regexp.MustCompile(`^\s*type\s+` + regexp.QuoteMeta(symbol) + `\b`), "type", 90},
		{regexp.MustCompile(`^\s*function\s+` + regexp.QuoteMeta(symbol) + `\b`), "function", 90},
		{regexp.MustCompile(`^\s*(?:async\s+)?(?:const|let|var)\s+` + regexp.QuoteMeta(symbol) + `\s*=\s*(?:async\s*)?\(`), "function", 88},
		{regexp.MustCompile(`^\s*(?:const|let|var)\s+` + regexp.QuoteMeta(symbol) + `\b`), "variable", 75},
		{regexp.MustCompile(`^\s*` + regexp.QuoteMeta(symbol) + `\s*[:=]\s*(?:async\s*)?\(`), "function", 82},
		{regexp.MustCompile(`^\s*` + regexp.QuoteMeta(symbol) + `\s*\(`), "call_or_definition", 45},
	}

	lines := strings.Split(content, "\n")
	var matches []symbolMatch
	for i, line := range lines {
		for _, candidate := range candidates {
			if candidate.re.MatchString(line) {
				matches = append(matches, symbolMatch{
					Path:    relPath,
					Line:    i + 1,
					Preview: strings.TrimSpace(line),
					Kind:    candidate.kind,
					Score:   candidate.score,
				})
				break
			}
		}
	}
	return matches
}

func previewLine(lines []string, line int) string {
	if line <= 0 || line > len(lines) {
		return ""
	}
	return strings.TrimSpace(lines[line-1])
}

