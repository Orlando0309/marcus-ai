func (t *SearchCodeTool) Run(ctx context.Context, input json.RawMessage) (json.RawMessage, error) {
	var params struct {
		Pattern    string `json:"pattern"`
		Path       string `json:"path"`
		Regex      bool   `json:"regex"`
		MaxResults int    `json:"max_results"`
	}
	if err := json.Unmarshal(input, &params); err != nil {
		return nil, fmt.Errorf("unmarshal input: %w", err)
	}
	if strings.TrimSpace(params.Pattern) == "" {
		return nil, fmt.Errorf("pattern is required")
	}
	if params.MaxResults <= 0 {
		params.MaxResults = 20
	}

	searchRoot := t.baseDir
	if params.Path != "" {
		resolved, err := resolveToolPath(t.baseDir, params.Path)
		if err != nil {
			return nil, err
		}
		searchRoot = resolved
	}
	if searchRoot == "" {
		searchRoot, _ = os.Getwd()
	}

	if t.index != nil {
		matches := t.index.Search(params.Pattern, params.Path, params.Regex, params.MaxResults)
		return json.Marshal(map[string]any{
			"pattern": params.Pattern,
			"count":   len(matches),
			"matches": matches,
		})
	}

	var matcher *regexp.Regexp
	var err error
	if params.Regex {
		matcher, err = regexp.Compile(params.Pattern)
		if err != nil {
			return nil, fmt.Errorf("compile regex: %w", err)
		}
	}

	baseDir := t.baseDir
	if baseDir == "" {
		baseDir = searchRoot
	}

	type match struct {
		Path    string `json:"path"`
		Line    int    `json:"line"`
		Preview string `json:"preview"`
	}
	var matches []match

	err = filepath.WalkDir(searchRoot, func(path string, d os.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		select {
		case <-ctx.Done():
			return ctx.Err()
		default:
		}
		if d.IsDir() {
			name := d.Name()
			if name == ".git" || name == "node_modules" || name == ".venv" || name == "__pycache__" {
				return filepath.SkipDir
			}
			return nil
		}
		info, err := d.Info()
		if err != nil || info.Size() > 1_000_000 {
			return nil
		}
		content, err := os.ReadFile(path)
		if err != nil {
			return nil
		}
		lines := strings.Split(string(content), "\n")
		for i, line := range lines {
			matched := false
			if params.Regex {
				matched = matcher.MatchString(line)
			} else {
				matched = strings.Contains(line, params.Pattern)
			}
			if matched {
				rel, _ := filepath.Rel(baseDir, path)
				matches = append(matches, match{
					Path:    filepath.ToSlash(rel),
					Line:    i + 1,
					Preview: strings.TrimSpace(line),
				})
				if len(matches) >= params.MaxResults {
					return filepath.SkipAll
				}
			}
		}
		return nil
	})
	if err != nil && err != filepath.SkipAll {
		return nil, err
	}

	sort.Slice(matches, func(i, j int) bool {
		if matches[i].Path == matches[j].Path {
			return matches[i].Line < matches[j].Line
		}
		return matches[i].Path < matches[j].Path
	})

	return json.Marshal(map[string]any{
		"pattern": params.Pattern,
		"count":   len(matches),
		"matches": matches,
	})
}

