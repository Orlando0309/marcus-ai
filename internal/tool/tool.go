// ToolRunner executes tools
type ToolRunner struct {
	baseDir       string
	commandPolicy safety.RunCommandPolicy
	tools         map[string]Tool
	definitions   map[string]Definition
	allowed       map[string]struct{}
	hooks         config.HooksConfig
}

// NewToolRunner creates a new tool runner
func NewToolRunner() *ToolRunner {
	return &ToolRunner{
		tools:       make(map[string]Tool),
		definitions: make(map[string]Definition),
	}
}

// Register registers a tool
func (tr *ToolRunner) Register(tool Tool) {
	tr.RegisterWithDefinition(tool, defaultDefinition(tool))
}

// Run executes a tool by name
func (tr *ToolRunner) Run(ctx context.Context, name string, input json.RawMessage) (json.RawMessage, error) {
	if err := tr.ensureAllowed(name); err != nil {
		return nil, err
	}
	if err := tr.runHooks(ctx, tr.hooks.PreToolUse, name, input, nil, nil, "tool"); err != nil {
		return nil, err
	}
	tool, ok := tr.tools[name]
	if !ok {
		return nil, fmt.Errorf("tool not found: %s", name)
	}
	out, err := tool.Run(ctx, input)
	if hookErr := tr.runHooks(ctx, tr.hooks.PostToolUse, name, input, out, err, "tool"); hookErr != nil && err == nil {
		err = hookErr
	}
	return out, err
}

// List returns the names of all registered tools.
func (tr *ToolRunner) List() []string {
	names := make([]string, 0, len(tr.tools))
	for name := range tr.tools {
		names = append(names, name)
	}
	return names
}

// ActionProposal is a structured action generated by Marcus.
type ActionProposal struct {
	ID          string         `json:"id"`
	Type        string         `json:"type"`
	Input       map[string]any `json:"input,omitempty"`
	Path        string         `json:"path,omitempty"`
	Content     string         `json:"content,omitempty"`
	Append      bool           `json:"append,omitempty"`
	Command     string         `json:"command,omitempty"`
	Dir         string         `json:"dir,omitempty"`
	Pattern     string         `json:"pattern,omitempty"`
	Symbol      string         `json:"symbol,omitempty"`
	Regex       bool           `json:"regex,omitempty"`
	MaxResults  int            `json:"max_results,omitempty"`
	Reason      string         `json:"reason,omitempty"`
	OldString   string         `json:"old_string,omitempty"`
	NewString   string         `json:"new_string,omitempty"`
	ReplaceAll  bool           `json:"replace_all,omitempty"`
	UnifiedDiff string         `json:"unified_diff,omitempty"`
}

func (a ActionProposal) Label() string {
	switch a.Type {
	case "write_file":
		return fmt.Sprintf("write %s", a.Path)
	case "run_command":
		return fmt.Sprintf("run %s", a.Command)
	case "search_code":
		return fmt.Sprintf("search %s", a.Pattern)
	case "list_files":
		return fmt.Sprintf("list files in %s", valueOr(a.Path, "."))
	case "read_file":
		return fmt.Sprintf("read %s", a.Path)
	case "find_symbol":
		return fmt.Sprintf("find symbol %s", a.Symbol)
	case "patch_file":
		return fmt.Sprintf("patch %s", a.Path)
	case "edit_file":
		return fmt.Sprintf("edit %s", a.Path)
	case "glob_files":
		return fmt.Sprintf("glob %s", a.Pattern)
	case "list_directory":
		return fmt.Sprintf("list dir %s", valueOr(a.Path, "."))
	case "todo_write":
		return "update todo checklist"
	case "task_tool":
		return "run isolated subagent"
	default:
		return "run " + a.Type
	}
}

func (a ActionProposal) Payload() json.RawMessage {
	if len(a.Input) > 0 {
		payload, _ := json.Marshal(a.Input)
		return payload
	}
	switch a.Type {
	case "patch_file":
		ud := a.UnifiedDiff
		if ud == "" {
			ud = a.Content
		}
		payload, _ := json.Marshal(map[string]any{
			"path":         a.Path,
			"unified_diff": ud,
		})
		return payload
	case "edit_file":
		payload, _ := json.Marshal(map[string]any{
			"path":        a.Path,
			"old_string":  a.OldString,
			"new_string":  a.NewString,
			"replace_all": a.ReplaceAll,
		})
		return payload
	case "glob_files":
		payload, _ := json.Marshal(map[string]any{
			"path":        a.Path,
			"pattern":     a.Pattern,
			"max_results": a.MaxResults,
		})
		return payload
	case "list_directory":
		payload, _ := json.Marshal(map[string]any{
			"path":        a.Path,
			"max_results": a.MaxResults,
		})
		return payload
	}
	payload, _ := json.Marshal(map[string]any{
		"path":        a.Path,
		"content":     a.Content,
		"append":      a.Append,
		"command":     a.Command,
		"dir":         a.Dir,
		"pattern":     a.Pattern,
		"symbol":      a.Symbol,
		"regex":       a.Regex,
		"max_results": a.MaxResults,
	})
	return payload
}

// ActionPreview is a non-destructive view of a proposal.
type ActionPreview struct {
	Proposal ActionProposal
	Summary  string
	Diff     string
}

// ActionResult captures the result of an applied action.
type ActionResult struct {
	Proposal ActionProposal
	Summary  string
	Diff     string
	Output   string
	ExitCode int
	Success  bool
}

// PreviewAction generates a preview for a proposal without mutating state.
func (tr *ToolRunner) PreviewAction(proposal ActionProposal) (ActionPreview, error) {
	if err := tr.validateActionProposal(proposal); err != nil {
		return ActionPreview{}, err
	}
	switch proposal.Type {
	case "write_file":
		path, err := tr.resolvePath(proposal.Path)
		if err != nil {
			return ActionPreview{}, err
		}
		current, readErr := os.ReadFile(path)
		if readErr != nil && !isNotExist(readErr) {
			return ActionPreview{}, fmt.Errorf("read current file: %w", readErr)
		}
		next := proposal.Content
		if proposal.Append && len(current) > 0 {
			next = string(current) + proposal.Content
		}
		d, err := diff.GenerateDiff(string(current), next)
		if err != nil {
			return ActionPreview{}, fmt.Errorf("generate diff: %w", err)
		}
		summary := fmt.Sprintf("Write %d bytes to %s", len(next), proposal.Path)
		if isNotExist(readErr) {
			summary = fmt.Sprintf("Create %s (%d bytes)", proposal.Path, len(next))
		}
		return ActionPreview{Proposal: proposal, Summary: summary, Diff: d}, nil
	case "run_command":
		dir := proposal.Dir
		if dir == "" {
			dir = tr.baseDir
		}
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("Run %q in %s", proposal.Command, dir),
		}, nil
	case "search_code":
		scope := proposal.Path
		if scope == "" {
			scope = "."
		}
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("Search for %q in %s", proposal.Pattern, scope),
		}, nil
	case "list_files":
		scope := proposal.Path
		if scope == "" {
			scope = "."
		}
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("List files in %s", scope),
		}, nil
	case "read_file":
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("Read %s", proposal.Path),
		}, nil
	case "find_symbol":
		scope := proposal.Path
		if scope == "" {
			scope = "."
		}
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("Find symbol %q in %s", proposal.Symbol, scope),
		}, nil
	case "patch_file":
		path, err := tr.resolvePath(proposal.Path)
		if err != nil {
			return ActionPreview{}, err
		}
		current, readErr := os.ReadFile(path)
		if readErr != nil {
			return ActionPreview{}, fmt.Errorf("read file: %w", readErr)
		}
		ud := proposal.UnifiedDiff
		if ud == "" {
			ud = proposal.Content
		}
		patches, err := diff.ParseUnifiedDiff(ud)
		if err != nil {
			return ActionPreview{}, err
		}
		next, err := diff.ApplyPatch(string(current), patches)
		if err != nil {
			return ActionPreview{}, err
		}
		d, err := diff.GenerateDiff(string(current), next)
		if err != nil {
			return ActionPreview{}, err
		}
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("Patch %s (%d → %d bytes)", proposal.Path, len(current), len(next)),
			Diff:     d,
		}, nil
	case "edit_file":
		path, err := tr.resolvePath(proposal.Path)
		if err != nil {
			return ActionPreview{}, err
		}
		current, err := os.ReadFile(path)
		if err != nil {
			return ActionPreview{}, fmt.Errorf("read file: %w", err)
		}
		if proposal.OldString == "" {
			return ActionPreview{}, fmt.Errorf("old_string is required")
		}
		s := string(current)
		var next string
		var n int
		if proposal.ReplaceAll {
			n = strings.Count(s, proposal.OldString)
			if n == 0 {
				return ActionPreview{}, fmt.Errorf("old_string not found")
			}
			next = strings.ReplaceAll(s, proposal.OldString, proposal.NewString)
		} else {
			n = strings.Count(s, proposal.OldString)
			if n == 0 {
				return ActionPreview{}, fmt.Errorf("old_string not found")
			}
			if n != 1 {
				return ActionPreview{}, fmt.Errorf("old_string matches %d times; use replace_all or narrow the string", n)
			}
			next = strings.Replace(s, proposal.OldString, proposal.NewString, 1)
		}
		d, err := diff.GenerateDiff(s, next)
		if err != nil {
			return ActionPreview{}, err
		}
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("Edit %s (%d replacement(s))", proposal.Path, n),
			Diff:     d,
		}, nil
	case "glob_files":
		scope := proposal.Path
		if scope == "" {
			scope = "."
		}
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("Glob %q under %s", proposal.Pattern, scope),
		}, nil
	case "list_directory":
		return ActionPreview{
			Proposal: proposal,
			Summary:  fmt.Sprintf("List directory %s", valueOr(proposal.Path, ".")),
		}, nil
	default:
		if def, ok := tr.Definition(proposal.Type); ok {
			return ActionPreview{
				Proposal: proposal,
				Summary:  fmt.Sprintf("Run tool %s (%s)", def.Name, def.Description),
			}, nil
		}
		return ActionPreview{}, fmt.Errorf("unsupported proposal type: %s", proposal.Type)
	}
}

// ApplyAction executes a structured proposal.
func (tr *ToolRunner) ApplyAction(ctx context.Context, proposal ActionProposal) (ActionResult, error) {
	preview, err := tr.PreviewAction(proposal)
	if err != nil {
		return ActionResult{}, err
	}
	input := proposal.Payload()
	if err := tr.runHooks(ctx, tr.hooks.PreToolUse, proposal.Type, input, nil, nil, "action"); err != nil {
		return ActionResult{}, err
	}
	finalize := func(result ActionResult, runErr error) (ActionResult, error) {
		payload, _ := json.Marshal(result)
		if hookErr := tr.runHooks(ctx, tr.hooks.PostToolUse, proposal.Type, input, payload, runErr, "action"); hookErr != nil && runErr == nil {
			runErr = hookErr
		}
		return result, runErr
	}
	switch proposal.Type {
	case "write_file":
		payload, _ := json.Marshal(map[string]any{
			"path":    proposal.Path,
			"content": proposal.Content,
			"append":  proposal.Append,
		})
		if _, err := tr.Run(ctx, "write_file", payload); err != nil {
			return finalize(ActionResult{}, err)
		}
		if listTool, ok := tr.tools["list_files"].(*ListFilesTool); ok {
			listTool.invalidate()
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Diff:     preview.Diff,
			Success:  true,
		}, nil)
	case "run_command":
		payload, _ := json.Marshal(map[string]any{
			"command": proposal.Command,
			"dir":     proposal.Dir,
		})
		out, err := tr.Run(ctx, "run_command", payload)
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		var result struct {
			Output   string `json:"output"`
			ExitCode int    `json:"exit_code"`
		}
		_ = json.Unmarshal(out, &result)
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   result.Output,
			ExitCode: result.ExitCode,
			Success:  result.ExitCode == 0,
		}, nil)
	case "search_code":
		payload, _ := json.Marshal(map[string]any{
			"pattern":     proposal.Pattern,
			"path":        proposal.Path,
			"regex":       proposal.Regex,
			"max_results": proposal.MaxResults,
		})
		out, err := tr.Run(ctx, "search_code", payload)
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   formatSearchOutput(out),
			Success:  true,
		}, nil)
	case "list_files":
		payload, _ := json.Marshal(map[string]any{
			"path":        proposal.Path,
			"max_results": proposal.MaxResults,
		})
		out, err := tr.Run(ctx, "list_files", payload)
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   formatListFilesOutput(out),
			Success:  true,
		}, nil)
	case "read_file":
		payload, _ := json.Marshal(map[string]any{
			"path": proposal.Path,
		})
		out, err := tr.Run(ctx, "read_file", payload)
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   formatReadFileOutput(out),
			Success:  true,
		}, nil)
	case "find_symbol":
		out, err := tr.Run(ctx, "find_symbol", proposal.Payload())
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   formatFindSymbolOutput(out),
			Success:  true,
		}, nil)
	case "patch_file":
		if _, err := tr.Run(ctx, "patch_file", proposal.Payload()); err != nil {
			return finalize(ActionResult{}, err)
		}
		if listTool, ok := tr.tools["list_files"].(*ListFilesTool); ok {
			listTool.invalidate()
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Diff:     preview.Diff,
			Success:  true,
		}, nil)
	case "edit_file":
		if _, err := tr.Run(ctx, "edit_file", proposal.Payload()); err != nil {
			return finalize(ActionResult{}, err)
		}
		if listTool, ok := tr.tools["list_files"].(*ListFilesTool); ok {
			listTool.invalidate()
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Diff:     preview.Diff,
			Success:  true,
		}, nil)
	case "glob_files":
		out, err := tr.Run(ctx, "glob_files", proposal.Payload())
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   formatGlobFilesOutput(out),
			Success:  true,
		}, nil)
	case "list_directory":
		out, err := tr.Run(ctx, "list_directory", proposal.Payload())
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   formatListDirectoryOutput(out),
			Success:  true,
		}, nil)
	default:
		out, err := tr.Run(ctx, proposal.Type, proposal.Payload())
		if err != nil {
			return finalize(ActionResult{}, err)
		}
		return finalize(ActionResult{
			Proposal: proposal,
			Summary:  preview.Summary,
			Output:   formatGenericOutput(out),
			Success:  true,
		}, nil)
	}
}

func (tr *ToolRunner) resolvePath(path string) (string, error) {
	if filepath.IsAbs(path) {
		return ensureWithinBaseDir(tr.baseDir, path)
	}
	if tr.baseDir == "" {
		return filepath.Abs(path)
	}
	return ensureWithinBaseDir(tr.baseDir, filepath.Join(tr.baseDir, path))
}

// Tool defines the interface for a Marcus tool
type Tool interface {
	Name() string
	Description() string
	Schema() JSONSchema
	Run(ctx context.Context, input json.RawMessage) (json.RawMessage, error)
}

// JSONSchema defines a tool's input schema
type JSONSchema struct {
	Type       string                   `json:"type"`
	Properties map[string]*JSONProperty `json:"properties,omitempty"`
	Required   []string                 `json:"required,omitempty"`
}

type JSONProperty struct {
	Type        string `json:"type"`
	Description string `json:"description"`
}

// ReadFileTool reads a file's contents
type ReadFileTool struct {
	baseDir string
}

// NewReadFileTool creates a new read_file tool
func NewReadFileTool(baseDir string) *ReadFileTool {
	return &ReadFileTool{baseDir: baseDir}
}

func (t *ReadFileTool) Name() string        { return "read_file" }
func (t *ReadFileTool) Description() string { return "Read the contents of a file" }

func (t *ReadFileTool) Schema() JSONSchema {
	return JSONSchema{
		Type: "object",
		Properties: map[string]*JSONProperty{
			"path": {Type: "string", Description: "Path to the file"},
		},
		Required: []string{"path"},
	}
}

func (t *ReadFileTool) Run(ctx context.Context, input json.RawMessage) (json.RawMessage, error) {
	var params struct {
		Path string `json:"path"`
	}
	if err := json.Unmarshal(input, &params); err != nil {
		return nil, fmt.Errorf("unmarshal input: %w", err)
	}

	path, err := resolveToolPath(t.baseDir, params.Path)
	if err != nil {
		return nil, err
	}

	content, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("read file: %w", err)
	}

	// Track the mtime so we can detect external modifications
	_ = file.Track(path)

	result := map[string]any{
		"path":    params.Path,
		"content": string(content),
		"size":    len(content),
	}

	return json.Marshal(result)
}

// ListFilesTool lists files inside a directory.
type ListFilesTool struct {
	baseDir string
	mu      sync.RWMutex
	cached  []string
}

// NewListFilesTool creates a new list_files tool.
func NewListFilesTool(baseDir string) *ListFilesTool {
	return &ListFilesTool{baseDir: baseDir}
}

func (t *ListFilesTool) Name() string        { return "list_files" }
func (t *ListFilesTool) Description() string { return "List files inside a directory" }

func (t *ListFilesTool) Schema() JSONSchema {
	return JSONSchema{
		Type: "object",
		Properties: map[string]*JSONProperty{
			"path":        {Type: "string", Description: "Optional directory to inspect"},
			"max_results": {Type: "number", Description: "Maximum number of results"},
		},
	}
}

func (t *ListFilesTool) Run(ctx context.Context, input json.RawMessage) (json.RawMessage, error) {
	var params struct {
		Path       string `json:"path"`
		MaxResults int    `json:"max_results"`
	}
	if err := json.Unmarshal(input, &params); err != nil {
		return nil, fmt.Errorf("unmarshal input: %w", err)
	}
	if params.MaxResults <= 0 {
		params.MaxResults = 50
	}
	files, err := t.listFiles(ctx, params.Path)
	if err != nil {
		return nil, err
	}
	if len(files) > params.MaxResults {
		files = files[:params.MaxResults]
	}
	return json.Marshal(map[string]any{
		"path":  valueOr(params.Path, "."),
		"count": len(files),
		"files": files,
	})
}

func (t *ListFilesTool) listFiles(ctx context.Context, scope string) ([]string, error) {
	if t.baseDir == "" {
		t.baseDir, _ = os.Getwd()
	}
	t.mu.RLock()
	cached := append([]string(nil), t.cached...)
	t.mu.RUnlock()
	if len(cached) == 0 {
		var err error
		cached, err = t.buildCache(ctx)
		if err != nil {
			return nil, err
		}
	}
	scope = filepath.ToSlash(strings.TrimSpace(scope))
	if scope == "" || scope == "." {
		return cached, nil
	}
	var filtered []string
	for _, file := range cached {
		if file == scope || strings.HasPrefix(file, scope+"/") || strings.Contains(strings.ToLower(file), strings.ToLower(scope)) {
			filtered = append(filtered, file)
		}
	}
	return filtered, nil
}

func (t *ListFilesTool) buildCache(ctx context.Context) ([]string, error) {
	var files []string
	err := filepath.WalkDir(t.baseDir, func(path string, d os.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		select {
		case <-ctx.Done():
			return ctx.Err()
		default:
		}
		if d.IsDir() {
			name := d.Name()
			if name == ".git" || name == "node_modules" || name == ".venv" || name == "__pycache__" {
				return filepath.SkipDir
			}
			return nil
		}
		rel, _ := filepath.Rel(t.baseDir, path)
		files = append(files, filepath.ToSlash(rel))
		return nil
	})
	if err != nil {
		return nil, err
	}
	sort.Strings(files)
	t.mu.Lock()
	t.cached = append([]string(nil), files...)
	t.mu.Unlock()
	return files, nil
}

func (t *ListFilesTool) invalidate() {
	t.mu.Lock()
	t.cached = nil
	t.mu.Unlock()
}

// WriteFileTool writes content to a file
type WriteFileTool struct {
	baseDir string
}

// NewWriteFileTool creates a new write_file tool
func NewWriteFileTool(baseDir string) *WriteFileTool {
	return &WriteFileTool{baseDir: baseDir}
}

func (t *WriteFileTool) Name() string        { return "write_file" }
func (t *WriteFileTool) Description() string { return "Write content to a file" }

func (t *WriteFileTool) Schema() JSONSchema {
	return JSONSchema{
		Type: "object",
		Properties: map[string]*JSONProperty{
			"path":    {Type: "string", Description: "Path to the file"},
			"content": {Type: "string", Description: "Content to write"},
			"append":  {Type: "boolean", Description: "Append instead of overwrite"},
		},
		Required: []string{"path", "content"},
	}
}

func (t *WriteFileTool) Run(ctx context.Context, input json.RawMessage) (json.RawMessage, error) {
	var params struct {
		Path    string `json:"path"`
		Content string `json:"content"`
		Append  bool   `json:"append"`
	}
	if err := json.Unmarshal(input, &params); err != nil {
		return nil, fmt.Errorf("unmarshal input: %w", err)
	}

	path, err := resolveToolPath(t.baseDir, params.Path)
	if err != nil {
		return nil, err
	}

	// Check that file hasn't been modified since we last read it
	if !params.Append {
		if err := file.Assert(path); err != nil {
			return nil, fmt.Errorf("stale file (possible race condition): %w", err)
		}
	}

	// Create parent directory if needed
	dir := filepath.Dir(path)
	if err := os.MkdirAll(dir, 0755); err != nil {
		return nil, fmt.Errorf("create dir: %w", err)
	}

	if params.Append {
		f, err := os.OpenFile(path, os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0644)
		if err != nil {
			return nil, fmt.Errorf("open append file: %w", err)
		}
		defer f.Close()
		if _, err := f.WriteString(params.Content); err != nil {
			return nil, fmt.Errorf("append file: %w", err)
		}
	} else {
		if err := os.WriteFile(path, []byte(params.Content), 0644); err != nil {
			return nil, fmt.Errorf("write file: %w", err)
		}
		// Track the new mtime after successful write
		_ = file.Track(path)
	}

	result := map[string]interface{}{
		"path":    params.Path,
		"written": true,
		"size":    len(params.Content),
	}

	return json.Marshal(result)
}

