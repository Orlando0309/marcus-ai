func (t *RunCommandTool) Run(ctx context.Context, input json.RawMessage) (json.RawMessage, error) {
	var params struct {
		Command string `json:"command"`
		Dir     string `json:"dir"`
	}
	if err := json.Unmarshal(input, &params); err != nil {
		return nil, fmt.Errorf("unmarshal input: %w", err)
	}
	params.Command = normalizeCommandForShell(params.Command)

	// Apply timeout if configured
	if t.timeout > 0 {
		var cancel context.CancelFunc
		ctx, cancel = context.WithTimeout(ctx, t.timeout)
		defer cancel()
	}

	// Build command
	var cmd *exec.Cmd
	if filepath.Separator == '\\' {
		if directCmd, ok := buildDirectWindowsCommand(ctx, params.Command); ok {
			cmd = directCmd
		} else {
			cmd = exec.CommandContext(ctx, "cmd", "/c", params.Command)
		}
	} else {
		cmd = exec.CommandContext(ctx, "sh", "-c", params.Command)
	}

	if params.Dir != "" {
		dir, err := resolveToolPath(t.dir, params.Dir)
		if err != nil {
			return nil, err
		}
		cmd.Dir = dir
	} else if t.dir != "" {
		cmd.Dir = t.dir
	}

	output, err := cmd.CombinedOutput()
	if err != nil {
		exitCode := 0
		if cmd.ProcessState != nil {
			exitCode = cmd.ProcessState.ExitCode()
		}
		return json.Marshal(map[string]any{
			"command":   params.Command,
			"exit_code": exitCode,
			"output":    string(output),
			"error":     err.Error(),
			"failed":    true,
		})
	}

	return json.Marshal(map[string]any{
		"command":   params.Command,
		"exit_code": 0,
		"output":    string(output),
		"failed":    false,
	})
}

func normalizeCommandForShell(command string) string {
	command = strings.TrimSpace(command)
	if command == "" {
		return command
	}
	if filepath.Separator == '\\' {
		// Models sometimes emit JSON-escaped quotes in commands. On Windows, Marcus
		// executes through `cmd /c`, so the shell should receive plain quotes.
		command = strings.ReplaceAll(command, `\"`, `"`)
		command = strings.ReplaceAll(command, `\'`, `'`)
		command = strings.ReplaceAll(command, "`\"", `"`)
		command = unwrapWrappedCommand(command)
	}
	return command
}

func buildDirectWindowsCommand(ctx context.Context, command string) (*exec.Cmd, bool) {
	if hasShellMetacharacters(command) {
		return nil, false
	}

	args, err := splitCommandLine(command)
	if err != nil || len(args) == 0 {
		return nil, false
	}
	if !strings.EqualFold(args[0], "git") {
		return nil, false
	}

	return exec.CommandContext(ctx, args[0], args[1:]...), true
}

func hasShellMetacharacters(command string) bool {
	return strings.ContainsAny(command, "|&;<>\n\r")
}

func unwrapWrappedCommand(command string) string {
	command = strings.TrimSpace(command)
	if len(command) < 2 {
		return command
	}
	if (command[0] == '"' && command[len(command)-1] == '"') || (command[0] == '\'' && command[len(command)-1] == '\'') {
		return strings.TrimSpace(command[1 : len(command)-1])
	}
	return command
}

func splitCommandLine(command string) ([]string, error) {
	var args []string
	var current strings.Builder
	var quote rune
	escaped := false

	flush := func() {
		if current.Len() == 0 {
			return
		}
		args = append(args, current.String())
		current.Reset()
	}

	for _, r := range command {
		switch {
		case escaped:
			current.WriteRune(r)
			escaped = false
		case r == '\\' && quote != '\'':
			escaped = true
		case quote != 0:
			if r == quote {
				quote = 0
			} else {
				current.WriteRune(r)
			}
		case r == '"' || r == '\'':
			quote = r
		case r == ' ' || r == '\t':
			flush()
		default:
			current.WriteRune(r)
		}
	}

	if escaped {
		current.WriteRune('\\')
	}
	if quote != 0 {
		return nil, fmt.Errorf("unterminated quote in command")
	}
	flush()
	return args, nil
}

func resolveToolPath(baseDir, path string) (string, error) {
	path = strings.TrimSpace(path)
	if path == "" {
		return "", nil
	}
	if strings.ContainsRune(path, 0) {
		return "", fmt.Errorf("path contains NUL")
	}
	if len(path) > 4096 {
		return "", fmt.Errorf("path too long")
	}
	if filepath.IsAbs(path) {
		return ensureWithinBaseDir(baseDir, path)
	}
	rel := filepath.Clean(path)
	if rel == ".." || strings.HasPrefix(rel, ".."+string(filepath.Separator)) {
		return "", fmt.Errorf("invalid relative path: %s", path)
	}
	if baseDir == "" {
		return filepath.Abs(path)
	}
	return ensureWithinBaseDir(baseDir, filepath.Join(baseDir, rel))
}

func ensureWithinBaseDir(baseDir, path string) (string, error) {
	absPath, err := filepath.Abs(path)
	if err != nil {
		return "", err
	}
	if baseDir == "" {
		return absPath, nil
	}
	absBase, err := filepath.Abs(baseDir)
	if err != nil {
		return "", err
	}
	evalPath := absPath
	if rp, err := filepath.EvalSymlinks(absPath); err == nil {
		evalPath = rp
	}
	evalBase := absBase
	if rb, err := filepath.EvalSymlinks(absBase); err == nil {
		evalBase = rb
	}
	rel, err := filepath.Rel(evalBase, evalPath)
	if err != nil {
		return "", err
	}
	if rel == ".." || strings.HasPrefix(rel, ".."+string(filepath.Separator)) {
		return "", fmt.Errorf("path escapes baseDir: %s", path)
	}
	return absPath, nil
}

